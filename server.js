function callGeminiOnce(systemPrompt, messages) {
  return new Promise((resolve, reject) => {
    const contents = messages.map((m) => ({
      role: m.role === 'assistant' ? 'model' : 'user',
      parts: [{ text: m.content }],
    }));

    const body = JSON.stringify({
      system_instruction: { parts: [{ text: systemPrompt }] },
      contents,
      generationConfig: { maxOutputTokens: 4096, temperature: 0.8 },
    });

    const req = https.request(
      {
        hostname: 'generativelanguage.googleapis.com',
        path: `/v1beta/models/gemini-2.5-flash:generateContent?key=${process.env.GOOGLE_API_KEY}`,
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Content-Length': Buffer.byteLength(body),
        },
      },
      (res) => {
        const chunks = [];
        res.on('data', (chunk) => chunks.push(chunk));
        res.on('end', () => {
          try {
            const data = Buffer.concat(chunks).toString('utf8');
            const json = JSON.parse(data);
            if (json.error) {
              const err = new Error(json.error.message);
              err.status = json.error.code;
              const m = json.error.message.match(/retry in ([\d.]+)s/i);
              err.retryAfter = m ? Math.ceil(parseFloat(m[1])) + 1 : 15;
              return reject(err);
            }
            resolve(json.candidates?.[0]?.content?.parts?.[0]?.text || '');
          } catch (e) {
            reject(new Error('Invalid JSON from Gemini'));
          }
        });
      }
    );
    req.on('error', reject);
    req.write(body);
    req.end();
  });
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function callGemini(systemPrompt, messages) {
  try {
    return await callGeminiOnce(systemPrompt, messages);
  } catch (err) {
    if (err.status === 429 && err.retryAfter) {
      const wait = Math.min(err.retryAfter, 20) * 1000;
      console.log(`Gemini 429 â€” retrying in ${wait / 1000}s...`);
      await sleep(wait);
      return await callGeminiOnce(systemPrompt, messages);
    }
    throw err;
  }
}

