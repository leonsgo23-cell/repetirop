// Direct HTTPS call to Google Gemini v1beta API
function callGeminiOnce(systemPrompt, messages) {
  return new Promise((resolve, reject) => {
    const contents = messages.map((m) => {
      const parts = [];
      if (m.content) parts.push({ text: m.content });
      if (m.imageData && m.imageMimeType) {
        parts.push({ inline_data: { mime_type: m.imageMimeType, data: m.imageData } });
      }
      return { role: m.role === 'assistant' ? 'model' : 'user', parts };
    });

    const body = JSON.stringify({
      system_instruction: { parts: [{ text: systemPrompt }] },
      contents,
      generationConfig: { maxOutputTokens: 2048, temperature: 0.8 },
    });

    const req = https.request(
      {
        hostname: 'generativelanguage.googleapis.com',
        path: `/v1beta/models/gemini-2.0-flash:generateContent?key=${process.env.GOOGLE_API_KEY}`,
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Content-Length': Buffer.byteLength(body),
        },
      },
      (res) => {
        const chunks = [];
        res.on('data', (chunk) => chunks.push(chunk));
        res.on('end', () => {
          try {
            const data = Buffer.concat(chunks).toString('utf8');
            const json = JSON.parse(data);
            if (json.error) {
              const err = new Error(json.error.message);
              err.status = json.error.code;
              const m = json.error.message.match(/retry in ([\d.]+)s/i);
              err.retryAfter = m ? Math.ceil(parseFloat(m[1])) + 2 : 30;
              return reject(err);
            }
            resolve(json.candidates?.[0]?.content?.parts?.[0]?.text || '');
          } catch (e) {
            reject(new Error('Invalid JSON from Gemini'));
          }
        });
      }
    );

    // 30-second hard timeout â€” prevents hanging forever
    req.setTimeout(30000, () => {
      req.destroy();
      const err = new Error('Gemini timeout: no response in 30s');
      err.status = 504;
      reject(err);
    });

    req.on('error', (e) => {
      const err = new Error(e.message || 'Network error');
      err.status = 503;
      reject(err);
    });
    req.write(body);
    req.end();
  });
}

// No server-side retry â€” client already handles backoff with countdown.
// Server retry just wastes quota (doubles API calls on every failure).
async function callGemini(systemPrompt, messages) {
  return await callGeminiOnce(systemPrompt, messages);
}

